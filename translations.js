let currentLang = localStorage.getItem('jd_lang') || 'en';

function toggleLanguage() {
    currentLang = currentLang === 'en' ? 'kn' : 'en';
    localStorage.setItem('jd_lang', currentLang);
    applyTranslations();
}

function applyTranslations() {
    // Update toggle switch UI
    const langSwitch = document.getElementById('lang-switch');
    if (langSwitch) {
        langSwitch.setAttribute('data-lang', currentLang);
    }

    // Translate DOM elements
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (translations[currentLang] && translations[currentLang][key]) {
            if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
                el.placeholder = translations[currentLang][key];
            } else if (el.tagName === 'OPTION') {
                el.textContent = translations[currentLang][key];
            } else {
                const icon = el.querySelector('i[data-lucide]');
                if (icon) {
                    const iconHTML = icon.outerHTML;
                    el.innerHTML = iconHTML + ' ' + translations[currentLang][key];
                } else {
                    el.innerHTML = translations[currentLang][key];
                }
            }
        }
    });
    
    if (typeof lucide !== 'undefined') {
        lucide.createIcons();
    }
}

document.addEventListener('DOMContentLoaded', () => {
    applyTranslations();
});
